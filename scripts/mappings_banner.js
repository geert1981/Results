// FUNCTIE VOOR HET ESCAPEN VAN REGULIERE EXPRESSIE TEKENS
function RegExp_escape(s) {
    return s.replace(/[-\/\\^$*+?.()|[\]{}]/g, '\\$&');
}

// DEEL VAN STRING VERVANGEN
function vervang_club_naam_banner(zoekterm_club_names_banner) {
    for (let i = 0; i < search_club_names_banner.length; i++) {
        const search_term_club_names_banner = search_club_names_banner[i];
        const replacement_club_names_banner = replace_club_names_banner[i];

        // CHECK OF ZOEKTERM DEEL UITMAAKT VAN HUIDIGE REEKSNAAM
        if (zoekterm_club_names_banner.includes(search_term_club_names_banner)) {
            // console.log('Zoekterm_club_name gevonden');
            // VERVANG DEEL VAN DE ZOEKTERM
            return zoekterm_club_names_banner.replace(new RegExp(RegExp_escape(search_term_club_names_banner), 'g'), replacement_club_names_banner);
        }
    }
    // ZOEKTERM NIET GEVONDEN -> TOON ORIGINELE NAAM
    // console.log('Zoekterm_club_name niet gevonden');
    return zoekterm_club_names_banner;
}